package LOOPINGSTMNT;

public class WhileLoopDemo4 {
    public static void main(String[] args) {
        int a = 10;
        while(a>10)
        {
            a--;
        }
        System.out.println(a);

        int b = 10;
        do {
            b--;
        }while(b>10);
        System.out.println(b);
    }
}
